name="Neha Pal"

#to print in upper case
print(name.upper())

# to print in lower case
print(name.lower())

#to find a word
print(name.find('e'))
print(name.find('N'))
print(name.find('Pal'))

#to replace a word
print(name.replace("Neha Pal", "Piya Pal"))

#to print the original name
print(name)

#to find a string as a true false
print("Pal" in name) 
print('t' in name)